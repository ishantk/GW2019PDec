# HashCode of Storage Container
print("id of age is:", id(age))
print("id of name is:", id(name))
print("id of marks is:", id(marks))

print("hex id of age is:", hex(id(age)))
print("hex id of name is:", hex(id(name)))
print("hex id of marks is:", hex(id(marks)))

print("oct id of age is:", oct(id(age)))
print("oct id of name is:", oct(id(name)))
print("oct id of marks is:", oct(id(marks)))

print("bin id of age is:", bin(id(age)))
print("bin id of name is:", bin(id(name)))
print("bin id of marks is:", bin(id(marks)))

