# Data is stored as Indexing Technique
# Data is Ordered
# Duplicates Allowed
names1 = ["John", "Jennie", "Jim", "Jack", "Joe", "Jennie"]
names2 = ("John", "Jennie", "Jim", "Jack", "Joe", "Jennie")

# Data is stored as Hashing Technique
# Data is Unordered
# Duplicates Not Allowed -> Uniqueness
names3 = {"John", "Jennie", "Jim", "Jack", "Joe", "Jennie"}

print(names1, type(names1))
print(names2, type(names2))
print(names3, type(names3))

