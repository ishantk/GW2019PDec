names1 = ["John", "Jenni", "Jim"]
print(names1, type(names1))

print()

# Convert it to Tuple
names2 = tuple(names1)
print(names2, type(names2))

print()

# Convert it to Set
names3 = set(names2)
print(names3, type(names3))

print()

# Convert it to list
names4 = list(names3)
print(names4, type(names4))

